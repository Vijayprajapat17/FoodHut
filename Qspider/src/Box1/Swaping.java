package Box1;


public class Swaping {
	public static void main(String[]args) {
		int a = 10;
		int b = 20;
		int c = 30;
		int d = 40;
		int temp = a;
		a = c;
		c = b;
		b = d;
		d = temp;
		
		System.out.println("a = "+a);
		System.out.println("b ="+b);
		System.out.println("c ="+c);
		System.out.println("d ="+d);
		
		
	}

}
