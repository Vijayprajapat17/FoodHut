package Box1;

public class Relational {
	public static void main(String []args) {
	int a=20;
	int b=40;
	System.out.println(a<b);
	System.out.println(a>b);
	System.out.println(a>=b);
	System.out.println(a<=b);
	System.out.println(a==b);
	System.out.println(a>15);
	System.out.println(a>'A');
	System.out.println(a!=b);

	}

}
